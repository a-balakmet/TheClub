fun String.isOnlyLetters(): Boolean {
    val booleanArray: ArrayList<Boolean> = ArrayList()
    for (i in this.indices) {
        val char = this[i]
        if (acceptableChars.contains(char)) booleanArray.add(true) else booleanArray.add(false)
    }
    return !booleanArray.contains(false)
}

fun String.isEmailValid(): Boolean = !TextUtils.isEmpty(this) && android.util.Patterns.EMAIL_ADDRESS.matcher(this).matches()